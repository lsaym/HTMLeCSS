// FOR: quando eu souber a quantidade de vezes que precisa-se iterar (repetir)
//while e do while: quando eu não souber a quantidae de vezes que preciso iterar
for(let i=0;i<10;i++){
    if(i%2==0){
        console.log(i + " É par")
    }else{
        console.log(i + " É impar")
    }
} 
//i>10: mostra quantas vezes será iterado
